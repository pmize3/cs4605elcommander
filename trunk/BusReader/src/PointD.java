
public class PointD {

	private double x;
	private double y;
	
	public PointD(double inX, double inY){
		x = inX;
		y = inY;
	}
	
	public double getX() {
		return x;
	}
	
	public void setX(double x) {
		this.x = x;
	}
	
	public double getY() {
		return y;
	}
	
	public void setY(double y) {
		this.y = y;
	}
}
